#include "stdlib.h"

int		*tabcpy(int *src)
{
	int *tab;
	int i;

	i = 0;
	tab = (int*)malloc(sizeof(int) * 81);
	while (i < 81)
	{
		tab[i] = src[i];
		i++;
	}
	return (tab);
}
