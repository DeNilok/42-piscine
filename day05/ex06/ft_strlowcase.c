char *ft_strlowcase(char *str)
{
	int i;
	int size;

	i = 0;
	size = 0;
	while (str[size] != '\0')
		size++;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}
