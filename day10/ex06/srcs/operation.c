#include "util.h"

int		sub(int i, int j)
{
	return (i - j);
}

int		div(int i, int j)
{
	return (i / j);
}

int		mod(int i, int j)
{
	return (i % j);
}

int		add(int i, int j)
{
	return (i + j);
}

int		multiply(int i, int j)
{
	return (i * j);
}
